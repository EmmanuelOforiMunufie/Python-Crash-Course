numbers = list(range(1,10))

for number in numbers:
    if number == 1:
        print("1st")
    elif number == 2:
        print("\n2nd")
    elif number == 3:
        print("\n3rd")
    elif number == 4:
        print("\n4th")
    elif number == 5:
        print("\n5th")
    elif number == 6:
        print("\n6th")
    elif number == 7:
        print("\n7th")
    elif number == 8:
        print("\n8th")
    else:
        print("\n9th")    